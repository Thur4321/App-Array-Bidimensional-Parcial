    private void jBRetornaIndiceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBRetornaIndiceActionPerformed
        int numero = Integer.parseInt(JOptionPane.showInputDialog(null, "Qual número deseja procurar?"));

        if (matriz.retornaIndice(numero) > 0) {
            jTAResultado.setText(matriz.dados() + "\nO número procurado foi: " + numero + "\nO valor foi encontrado nas posições: " + matriz.retornaPosicao(numero));
        } else {
            jTAResultado.setText(matriz.dados() + "\nO valor não foi encontrado");
        }
    }//GEN-LAST:event_jBRetornaIndiceActionPerformed

