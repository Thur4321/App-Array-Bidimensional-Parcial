    public void setEstado(int estado) {
        this.estado = estado;
        if (estado == CRIACAO) {
            jBDefinirMatriz.setEnabled(true);
            jBGerarMatriz.setEnabled(false);
            jBDiagonaPrincipal.setEnabled(false);
            jBDiagSecundaria.setEnabled(false);
            jBSomaLinhas.setEnabled(false);
            jBSomaColunas.setEnabled(false);
            jBTransposta.setEnabled(false);
            jBBuscaValor.setEnabled(false);
            jBLimpar.setEnabled(false);
        } else if (estado == INICIALIZACAO) {
            jBGerarMatriz.setEnabled(linha >= 1 && coluna >= 1);
            jBDefinirMatriz.setEnabled(false);
            jBDiagonaPrincipal.setEnabled(false);
            jBDiagSecundaria.setEnabled(false);
            jBSomaLinhas.setEnabled(false);
            jBSomaColunas.setEnabled(false);
            jBTransposta.setEnabled(false);
            jBBuscaValor.setEnabled(false);
            jBLimpar.setEnabled(false);
        } else if (estado == OPERACAO) {
            jBDefinirMatriz.setEnabled(false);
            jBGerarMatriz.setEnabled(linha >= 1 && coluna >= 1);
            jBDiagonaPrincipal.setEnabled(matriz.ehQuadrada());
            jBDiagSecundaria.setEnabled(matriz.ehQuadrada());
            jBSomaLinhas.setEnabled(linha >= 1 && coluna >= 2);
            jBSomaColunas.setEnabled(linha >= 2 && coluna >= 1);
            jBTransposta.setEnabled(linha >= 2 && coluna >= 2);
            jBBuscaValor.setEnabled(linha >= 1 && coluna >= 1);
            jBLimpar.setEnabled(linha >= 1 && coluna >= 1);
        }

    }

